.section .note.GNU-stack,"",@progbits
.section .text

.global Gen_random
Gen_random:
	xor %eax, %eax	# clear the EAX register
	rdrand %eax	# read a random number generated by the hardware and store it in EAX
	jnc Gen_random	# in case of a valid random value are returned (CF = 1) the program returns otherwise (CF = 0) we try to regenerate new value
Finish:
	ret
